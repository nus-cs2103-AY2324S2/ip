package exceptions;

/**
 * Handles RyanGosling-related exceptions.
 */
public class RyanGoslingException extends Exception {
    /**
     * Constructs a new RyanGoslingException with the specified detail message.
     *
     * @param message The detail message (which is saved for later retrieval by the getMessage() method).
     */
    public RyanGoslingException(String message) {
        super(message);
    }
}
