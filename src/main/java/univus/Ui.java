package univus;

/**
 * The Ui class that deals with interactions with the user.
 */
public class Ui {
    /**
     * Prints a greeting to the user.
     */
    public String greet() {
        return "Hello! I'm Univus\n" + "What can I do for you?\n";
    }

    /**
     * prints a bye to the user.
     */
    public String bye() {
        return "Bye. Hope to see you again soon!\n";
    }
}
