package osiris.exceptions;

/**
 * The OsirisStorageFileException class represents an exception related to storage file operations.
 */
public class OsirisStorageFileException extends OsirisException {

    /**
     * Constructs a new OsirisStorageFileException with the specified error message.
     *
     * @param s The error message describing the storage file exception.
     */
    public OsirisStorageFileException(String s) {
        super(s);
    }
}
