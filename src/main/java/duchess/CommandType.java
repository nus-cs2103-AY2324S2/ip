package duchess;

/**
 * CommandType enum represents the types of commands available in the Duchess program.
 * It defines 9 command types: BYE, LIST, TODO, EVENT, DEADLINE, DELETE, FIND, MARK, UNMARK.
 */
public enum CommandType {
    BYE, LIST, TODO, EVENT, DEADLINE, DELETE, FIND, MARK, UNMARK, UNKNOWN, DUPLICATES
}
