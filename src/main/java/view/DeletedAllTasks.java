package view;

/**
 * The DeletedAllTasks class represents a view component for displaying a message when all tasks are deleted.
 * Extends UI.
 */
public class DeletedAllTasks extends UI {

    /**
     * Displays a message indicating that all tasks have been deleted.
     *
     * @return A string message indicating that all tasks have been deleted.
     */
    public static String display() {
        return "I've deleted all the tasks :D";
    }
}
