package duke.Ui;


/**
 * Represents the user interface of the application.
 * This class is responsible for managing user interactions by displaying errors and other messages.
 */
public class Ui {

    /**
     * Constructor for creating a UI object.
     */
    public Ui() {}

    /**
     * Displays an error message when there is a loading error.
     */
    public void showLoadingError() {
        System.out.println("Loading ERROR");
    }
}

