package edgar;

import javafx.application.Application;


/**
 * The Launcher class provides a main method to launch the JavaFX application.
 * It starts the application by calling the start method of the Main class.
 */
public class Launcher {
    /**
     * The main entry point for launching the JavaFX application.
     *
     * @param args Command-line arguments passed to the application (not used).
     */
    public static void main(String[] args) {
        Application.launch(Main.class, args);
    }
}
