package commands;

/**
 * The Constants class contains static final strings representing command keywords used in task management.
 * These constants are used to identify and interpret user input for various commands.
 */
public class Constants {

    /** Command keyword for listing tasks. */
    public static final String LIST = "list";

    /** Command keyword for marking a task as done. */
    public static final String MARK = "mark";

    /** Command keyword for unmarking a previously marked task. */
    public static final String UNMARK = "unmark";

    /** Command keyword for deleting a task. */
    public static final String DELETE = "delete";

    /** Command keyword for adding a todo task. */
    public static final String TODO = "todo";

    /** Command keyword for adding a deadline task. */
    public static final String DEADLINE = "deadline";

    /** Command keyword for adding an event task. */
    public static final String EVENT = "event";

    /** Command keyword for displaying help information. */
    public static final String HELP = "help";

    /** Command keyword to search for a task. */
    public static final String FIND = "find";

    /** Command keyword to snooze a task. */
    public static final String SNOOZE = "snooze";

}

